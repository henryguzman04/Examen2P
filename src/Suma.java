public class Suma {
    public int suma_e(int a1, int b1) {
        return a1 + b1;
    }

    public int suma_e(int a2, int b2, int c1) {
        return a2 + b2 + c1;
    }

    public double suma_d(double a3, double b3) {
        return a3 + b3;

    }
    public double suma_d(double a4, double b5, double c2) {
        return a4 + b5 + c2;
    }

}


